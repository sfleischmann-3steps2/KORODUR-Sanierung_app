export function getReferenzBySlug(slug: string): Referenz | undefined {
  return referenzen.find((r) => r.slug === slug);
}

export function getReferenzenByKategorie(kategorie: string): Referenz[] {
  return referenzen.filter((r) => r.kategorie === kategorie);
}

export function getReferenzenByUnterkategorie(
  kategorie: string,
  unterkategorie: string
): Referenz[] {
  return referenzen.filter(
    (r) => r.kategorie === kategorie && r.unterkategorie === unterkategorie
  );
}
